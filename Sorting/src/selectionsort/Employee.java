package selectionsort;

public class Employee {
	private int empid;
	private String ename;
	private int salary;
	public Employee(int empid, String ename, int salary) {
		super();
		this.empid = empid;
		this.ename = ename;
		this.salary = salary;
	}
	public int getSalary() {
		return salary;
	}
	
	
}
